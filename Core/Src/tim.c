// 舵机控制相关函数
void Servo_SetAngle(uint8_t angle)
{
  // 舵机角度范围：0-180度
  // PWM脉冲宽度范围：0.5ms-2.5ms
  // 对应占空比：2.5%-12.5%
  // TIM2配置：72MHz / (7199+1) = 10kHz
  // 周期：199，PWM周期为 (199+1)/10kHz = 20ms，正确
  
  if(angle > 180) angle = 180;
  
  // 计算脉冲宽度：0.5ms + (angle/180)*2ms
  // 对应TIM2的比较值：(0.5ms + (angle/180)*2ms) * 10kHz = 5 + (angle*20)/18
  uint16_t compare_value = 5 + (angle * 20) / 18;
  
  // 设置比较值
  __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_2, compare_value);
}

void Servo_Init(void)
{
  // TIM2已经在MX_TIM2_Init中配置，这里只需要启动PWM
  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_2);
  // 初始位置设为中间位置（90度）
  Servo_SetAngle(90);
}
